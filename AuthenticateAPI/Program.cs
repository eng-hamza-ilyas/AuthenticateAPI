using AuthenticateAPI.Context;
using AuthenticateAPI.Interfaces;
using AuthenticateAPI.Services;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using System.Text;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddDbContext<JwtContext>(options => options.UseSqlServer(builder.Configuration.GetConnectionString("Database")));
builder.Services.AddTransient<IEmployeService, EmployeService>();
builder.Services.AddTransient<IAuthService, AuthService>();

